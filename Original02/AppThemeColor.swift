import SwiftUI

/// アプリ全体で使う配色です。
enum AppThemeColor {
    static let baseBackground = Color(uiColor: .systemGroupedBackground)
    static let accent = Color(.sRGB, red: 74.0 / 255.0, green: 144.0 / 255.0, blue: 164.0 / 255.0, opacity: 1.0) // #4A90A4
    static let support = Color(.sRGB, red: 220.0 / 255.0, green: 238.0 / 255.0, blue: 239.0 / 255.0, opacity: 1.0) // #DCEEEF
    static let cardBackground = Color(uiColor: .secondarySystemBackground)
}
